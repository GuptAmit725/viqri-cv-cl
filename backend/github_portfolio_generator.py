    def __init__(self, github_token: str):
        """
        Initialize GitHub Portfolio Generator
        
        Args:
            github_token: GitHub Personal Access Token with repo permissions
        """
        self.github_token = github_token
        self.base_url = "https://api.github.com"
        self.headers = {
            "Authorization": f"token {github_token}",
            "Accept": "application/vnd.github.v3+json"
        }
    
    def verify_token(self) -> Dict:
        """
        Verify GitHub token is valid
        
        Returns:
            dict with success status and user info
        """
        try:
            response = requests.get(
                f"{self.base_url}/user",
                headers=self.headers
            )
            
            if response.status_code == 200:
                user_data = response.json()
                return {
                    "success": True,
                    "username": user_data.get("login"),
                    "name": user_data.get("name"),
                    "email": user_data.get("email")
                }
            else:
                return {
                    "success": False,
                    "error": "Invalid GitHub token"
                }
        except Exception as e:
            logger.error(f"Error verifying token: {str(e)}")
            return {
                "success": False,
                "error": str(e)
            }
    
    def create_repository(self, username: str, repo_name: str = None) -> Dict:
        """
        Create GitHub repository for portfolio
        
        Args:
            username: GitHub username
            repo_name: Repository name (defaults to username.github.io)
            
        Returns:
            dict with success status and repo info
        """
        if repo_name is None:
            repo_name = f"{username}.github.io"
        
        try:
            # Check if repo already exists
            check_response = requests.get(
                f"{self.base_url}/repos/{username}/{repo_name}",
                headers=self.headers
            )
            
            if check_response.status_code == 200:
                logger.info(f"Repository {repo_name} already exists")
                return {
                    "success": True,
                    "repo_name": repo_name,
                    "exists": True,
                    "message": "Repository already exists"
                }
            
            # Create new repository
            create_data = {
                "name": repo_name,
                "description": "My Professional Portfolio - Generated by Viqri CV",
                "homepage": f"https://{username}.github.io",
                "private": False,
                "auto_init": True
            }
            
            response = requests.post(
                f"{self.base_url}/user/repos",
                headers=self.headers,
                json=create_data
            )
            
            if response.status_code == 201:
                return {
                    "success": True,
                    "repo_name": repo_name,
                    "exists": False,
                    "message": "Repository created successfully"
                }
            else:
                return {
                    "success": False,
                    "error": f"Failed to create repository: {response.json().get('message', 'Unknown error')}"
                }
                
        except Exception as e:
            logger.error(f"Error creating repository: {str(e)}")
            return {
                "success": False,
                "error": str(e)
            }
    
