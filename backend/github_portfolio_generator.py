    def upload_file_to_repo(self, username: str, repo_name: str, 
                           file_path: str, content: str, 
                           commit_message: str = "Update portfolio") -> Dict:
        """
        Upload or update a file in GitHub repository
        
        Args:
            username: GitHub username
            repo_name: Repository name
            file_path: Path where file should be created (e.g., "index.html")
            content: File content
            commit_message: Git commit message
            
        Returns:
            dict with success status
        """
        try:
            # Check if file exists to get SHA
            file_url = f"{self.base_url}/repos/{username}/{repo_name}/contents/{file_path}"
            get_response = requests.get(file_url, headers=self.headers)
            
            sha = None
            if get_response.status_code == 200:
                sha = get_response.json().get("sha")
                logger.info(f"File {file_path} exists, will update")
            
            # Encode content to base64
            content_bytes = content.encode('utf-8')
            content_base64 = base64.b64encode(content_bytes).decode('utf-8')
            
            # Prepare data
            data = {
                "message": commit_message,
                "content": content_base64,
                "branch": "main"
            }
            
            if sha:
                data["sha"] = sha
            
            # Upload/update file
            response = requests.put(file_url, headers=self.headers, json=data)
            
            if response.status_code in [201, 200]:
                return {
                    "success": True,
                    "message": f"File {file_path} uploaded successfully"
                }
            else:
                error_msg = response.json().get('message', 'Unknown error')
                logger.error(f"Failed to upload file: {error_msg}")
                return {
                    "success": False,
                    "error": f"Failed to upload file: {error_msg}"
                }
                
        except Exception as e:
            logger.error(f"Error uploading file: {str(e)}")
            return {
                "success": False,
                "error": str(e)
            }
    
    def deploy_portfolio(self, cv_data: Dict, username: str, 
                        repo_name: str = None) -> Dict:
        """
        Complete portfolio deployment workflow
        
        Args:
            cv_data: Extracted CV data
            username: GitHub username
            repo_name: Repository name (optional)
            
        Returns:
            dict with deployment status and URL
        """
        try:
            # Step 1: Create repository
            logger.info("Creating repository...")
            repo_result = self.create_repository(username, repo_name)
            
            if not repo_result.get("success"):
                return repo_result
            
            actual_repo_name = repo_result.get("repo_name")
            
            # Step 2: Generate HTML
            logger.info("Generating portfolio HTML...")
            html_content = self.generate_portfolio_html(cv_data)
            
            # Step 3: Upload to GitHub
            logger.info("Uploading to GitHub...")
            upload_result = self.upload_file_to_repo(
                username=username,
                repo_name=actual_repo_name,
                file_path="index.html",
                content=html_content,
                commit_message="Deploy portfolio from Viqri CV"
            )
            
            if not upload_result.get("success"):
                return upload_result
            
            # Calculate portfolio URL
            portfolio_url = f"https://{username}.github.io"
            if actual_repo_name != f"{username}.github.io":
                portfolio_url = f"https://{username}.github.io/{actual_repo_name}"
            
            return {
                "success": True,
                "message": "Portfolio deployed successfully!",
                "portfolio_url": portfolio_url,
                "repo_name": actual_repo_name,
                "note": "Your portfolio will be live in a few minutes at the URL above"
            }
            
        except Exception as e:
            logger.error(f"Error deploying portfolio: {str(e)}")
            return {
                "success": False,
                "error": str(e)
            }